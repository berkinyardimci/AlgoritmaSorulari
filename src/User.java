
public class User {
	 String name;
	 String tcKimlik;
	
	
	public User(String name, String tcKimlik) {
		super();
		this.name = name;
		this.tcKimlik = tcKimlik;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public String getTcKimlik() {
		return tcKimlik;
	}

}
